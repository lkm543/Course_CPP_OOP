<h2>2-4：閏年判斷</h2>
	<h3>Input：年份</h3>
	<h3>Output：該年份是否為閏年</h3>
	<h3>Hint：閏年規則如下</h3>
	<h3>Rule 1：原則上年份可整除4，則為閏年。</h3>
	<h3>Rule 2：若年份可整除4，但可整除100，則為平年。</h3>
	<h3>Rule 3：若年份又可整除400，則為閏年。</h3>
