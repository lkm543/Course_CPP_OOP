<h2>大綱</h2>
<p>當我們一筆資料可能含有多種不同屬性的時候，我們便可以透過結構來開出一個新的資料型別，該資料型別底下便可以包含不同的變數型態，這種用法稱之為結構。同時結構的使用也可以做為物件的基礎。<br>(本章預計授課時間：40-50 Mins)</p> 

